		}

		public override void BindToName(Type serializedType, out string assemblyName, out string typeName)
		{
			string text = null;
			string text2 = null;
			assemblyName = null;
			typeName = null;
			if (this.innerBinder != null)
			{
				this.innerBinder.BindToName(serializedType, out text, out text2);
				this.InternalBindToName(serializedType, out assemblyName, out typeName);
				if (assemblyName == null && typeName == null)
				{
					assemblyName = text;
					typeName = text2;
				}
			}
		}

		public override Type BindToType(string assemblyName, string typeName)
		{
			this.ValidateTypeToDeserialize(assemblyName, typeName);
			Type type = null;
			Type type2 = this.InternalBindToType(assemblyName, typeName);
			if (this.innerBinder != null)
			{
				if (type2 != null)
				{
					assemblyName = type2.Assembly.FullName;
					typeName = type2.FullName;
				}
				type = this.innerBinder.BindToType(assemblyName, typeName);
			}
			return type2 ?? type;
		}

		protected virtual Type InternalBindToType(string assemblyName, string typeName)
		{
			return null;
		}

		protected virtual void InternalBindToName(Type serializedType, out string assemblyName, out string typeName)
		{
			assemblyName = null;
			typeName = null;
		}

		protected void ValidateTypeToDeserialize(string assemblyName, string typeName)
		{
			if (!this.allowedTypesForDeserialization.Contains(typeName))
			{
				if (ChainedSerializationBinder.DisallowedTypesForDeserialization.Contains(typeName))
				{
					throw new InvalidOperationException("Type" + typeName + "failed deserialization (BlockList).");
				}
				if (!ChainedSerializationBinder.GlobalAllowedTypesForDeserialization.Contains(typeName))
				{
					this.LogType(assemblyName, typeName);
				}
			}
		}

