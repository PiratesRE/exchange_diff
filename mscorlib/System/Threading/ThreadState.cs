using System;
using System.Runtime.InteropServices;

namespace System.Threading
{
	[Flags]
	[ComVisible(true)]
	[Serializable]
	public enum ThreadState
	{
		Running = 0,
		StopRequested = 1,
		SuspendRequested = 2,
		Background = 4,
		Unstarted = 8,
		Stopped = 16,
		WaitSleepJoin = 32,
		Suspended = 64,
		AbortRequested = 128,
		Aborted = 256
	}
}
