			}

			void IDisposable.Dispose()
			{
				this.Dispose(true);
			}

			public string Path
			{
				get
				{
					return this._path;
				}
			}

			private IStore _pSourceStore;

			private IntPtr _pLockCookie = IntPtr.Zero;

			private string _path;
		}

